[[eosio::action]] void CONTRACT_CLASS_NAME::close( name owner, const symbol& symbol ){

   require_auth( owner );
   
   accounts acnts( _self, owner.value );
   auto it = acnts.find( symbol.code().raw() );
   check( it != acnts.end(), "âš ï¸ Balance record already deleted or never existed âš ï¸" + ERROR_LOCATION );
   check( it -> balance.amount == 0, "âš ï¸ Cannot close because the balance is not zero âš ï¸" + ERROR_LOCATION );
   acnts.erase( it );
   print ( "ğŸ—‘ï¸ ", owner, " closed " , CACHE_SYM_STRING , " record ğŸ—‘ï¸ \n" ); 

}

